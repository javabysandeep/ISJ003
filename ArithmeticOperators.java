public class ArithmeticOperators {

	public static void main(String[] args){

		System.out.println(10 + 20); //30
		System.out.println(10 - 20); //-10
		System.out.println(10 * 20); // 200
		System.out.println(10 / 20); // Q = 0
		System.out.println(10 % 20); // R = 10

		System.out.println(10 / 1); // Q = 10
		System.out.println(10 % 1); // R = 0

		System.out.println(123 % 10); // R = 3
		System.out.println(123 / 10); // Q = 12

		System.out.println(12 % 10); // R = 2
		System.out.println(12 / 10); // Q = 1	

		System.out.println(1 % 10); // R = 1
		System.out.println(1 / 10); // Q = 0

		System.out.println(1.2 / 0); // Q =  Infinity
		System.out.println(1.2 % 0); // R = 1 NaN

		System.out.println(0 % 10); // R = 0
		System.out.println(0 / 10); // Q = 0

		System.out.println(1 % 0); // R = ArithmeticException divide by zero
		System.out.println(1 / 0); // Q = 1 ArithmeticException divide by zero




	}

}









