package controlStatements.looping.whileLoop;

public class WhileDemo09 {
    public static void main(String[] args) {
        int left = 0;
        int right = 10;
        while (left < right) {
            System.out.println("Left = " + left + "\t Right = " + right);
            right++;
        }
        //op: infinite ->

    }
}
