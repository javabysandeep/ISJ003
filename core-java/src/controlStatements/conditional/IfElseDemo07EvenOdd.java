package controlStatements.conditional;

public class IfElseDemo07EvenOdd {
    public static void main(String[] args) {
        int number = 11;
        if (number % 2 == 0) {
            System.out.println(number + " is a even number");
        } else {
            System.out.println(number + " is a odd number");
        }
    }


}
