package controlStatements.conditional;

public class IfElseDemo01 {
    public static void main(String[] args) {
        if (4 > 5) {
            System.out.println(1);
            System.out.println(2);
            System.out.println(3);
        }
        System.out.println(4);
        System.out.println(5);
    }
}
