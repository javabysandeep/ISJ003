package controlStatements.conditional.ifElseDemos;

public class IfElseDemo08MultipleOf7 {
    public static void main(String[] args) {
        int number = 29;
        if (number % 7 == 0) {
            System.out.println(number + " is a multiple of seven");
        } else {
            System.out.println(number + " is not a multiple of seven");
        }
    }


}
